 		function sendEmail(){
            var name = $("#name");
            var phone = $("#phone");
            var service = $("#service");
            var email = $("#email");
            var body = $("#body");
            var gdpr = $("#gdpr").required;

            if (isNotEmpty(name) && isNotEmpty(phone) && isNotEmpty(service) && isNotEmpty(email) && isNotEmpty(body) && isNotEmpty(gdpr)){
                $.ajax({
                    url: 'sendEmail.php',
                    method: 'POST',
                    dataType: 'json',
                    data:{
                        name: name.val(),
                        phone: phone.val(),
                        service: service.val(),
                        email: email.val(),
                        body: body.val(),
                        gdpr: gdpr.val(),

                    }, 
                    beforeSend: function(){
                        $("#loader").show();
                    },
                    complete:function(){
                        $("#loader").hide();
                    },

                    success: function(response){
                        $('#myForm')[0].reset();
                        $('.sent-notification')
                        swal({
                            text:"Message sent successfully.",
                        })
                        /*.text("Message sent successfully.")*/
                    },
                    error:function () {
                        $('.sent-notification')
                         swal({
                            text:" Invalid Email Detected ! ",
                        })

                        
                    }
                });
            }
        }
        function isNotEmpty(caller){
            if(caller.val()==""){
                caller.css('border', '1px solid red');
                return false;
            }
            else{
                caller.css('border', '');
                return true;
            }
        }

